package d1230;

public class DataType {
	public static void main(String[] args) {
		// 숫자, 문자, 논리
		// 정수
		byte b = -128; // -128 ~ 127
		short s; // 
		int i;
		long l;
		// 실수
		float f;
		double db;
		
		//문자
		char c = '1';
		
		//논리
		boolean bl = true;
		// 이탤릭체가 아니면 static이 아님
		// 이탤릭체면 static임.
		System.out.println(); // non-static
	}
}
